# 3x3 matrix
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# Printing each element individually
print(matrix[0][0])  # Output: 1
print(matrix[0][1])  # Output: 2
print(matrix[0][2])  # Output: 3

print(matrix[1][0])  # Output: 4
print(matrix[1][1])  # Output: 5
print(matrix[1][2])  # Output: 6

print(matrix[2][0])  # Output: 7
print(matrix[2][1])  # Output: 8
print(matrix[2][2])  # Output: 9

# You can also print the entire matrix
print(matrix)
